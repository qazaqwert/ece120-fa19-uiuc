		ADD  R2,R2,#8

LOADINIT	LD   R1,CHAR1	;Loads the address of the first char to be printed into R1

NEXTCHAR	AND  R4,R4,#0	;Making sure R4 is clear again
		ADD  R4,R4,#4	;This is the counter for the math needed to find the starting location in FONT_DATA for the character. 
		AND  R3,R3,#0	;Making sure R3 is clear again
		ADD  R3,R3,#8	;Counter for how many times to do left shifting
		LDR  R5,R1,#0   ;Loads the value from the address in R1 into R5
  
MATHLOOP1	ADD  R5,R5,R5	;Adds the starting R5 value to the running total
		ADD  R4,R4,#-1	;Decrements the counter
		BRnp MATHLOOP1	;Continues looping until counter is 0
		LEA  R0,FONT_DATA ;Loads the address of the first bit of font data into R0
		ADD  R5,R5,R0	;R5 Should now have the address of the starting line in the font data in it.
		ADD  R5,R5,R6   ;Increments R5 by X amount based on which line of sub-ascii we are on 
		BR   SUBLOOP1   ;Goes to the first line of printing

SUBLOOP1	LDR  R0,R1,#0   ;Temp loads the value at R1 into R0
		BRz  OWO	;If R0 is zero (NULL) it goes to line break
		LDR  R5,R5,#0	;Loads R5 with the contents of the font address line specified by R5
		BRn  NEGLOOP	;If R5 is negative (MSB = 1) it goes to print the 1 character
		BRzp  POSLOOP	;If R5 is positive (MSB = 0) it goes to print the 0 character

SUBLOOP2	ADD  R5,R5,0	;Sets cc with R5
		BRn  NEGLOOP	;If R5 is negative (MSB = 1) it goes to print the 1 character
		BRzp POSLOOP	;If R5 is positive (MSB = 0) it goes to print the 0 character

NEGLOOP		LDI  R0,ONE	;Loads R0 with the hex value for the ascii character defined in the case file
		OUT		;Prints the ascii char in R0 to the console
		BR   SUBSUBLOOP	;Heads to the step to increment (left shift) the font line's value

POSLOOP		LDI  R0,ZERO	;Loads R0 with the hex value for the ascii character defined in the case file
		OUT 		;Prints the ascii char in R0 to the console
		BR   SUBSUBLOOP	;Heads to the step to increment (left shift) the font line's value

SUBSUBLOOP	ADD  R5,R5,R5	;Multiplies R5 (the value of the current line) by 2, effectively left-shifting it	
		ADD  R3,R3,#-1	;Decrements the counter for number of characters printed
		BRnp SUBLOOP2	;If the counter isnt 0 it moves back to the loop to determine and print the next character in the line
		BRz  CHARINC	;If counter = 0, move to the char incrementation to print the line of the next character in the string
		
		;LD  R0,NEWLINE If counter = 0, This step runs which loads R0 with the new line character
		;OUT		Prints the new line character to the console
		;BR  MAINLOOP	Heads back to the main loop to increment the address pointer and decrement the line # counter. 



