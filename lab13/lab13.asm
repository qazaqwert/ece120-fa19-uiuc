		AND  R3,R3,0	;Left Shift counter
		AND  R4,R4,0	;Math counter
		AND  R5,R5,0	;Stores the address in font_data of the character and line that is being printed
		AND  R6,R6,0	;Address offset for each subsequent line
		
		
