;This program is supposed to print an ascii character defined in a different file to the console in a large font with certain ascii characters making up the large character, also defined by the other file
;There is a large table of font values to be used that is defined in the program. The two leftmost hex digits (so 8 leftmost binary digits) are used to define which ascii character goes where
;with a 0 being represented by one ascii character and 1 by another, both defined in a separate file
;All 256 possible ascii values that could be printed in the large font are held in 16 lines of these 8 digits which are defined sequentially in the font table
;To accomplish this, I first did math to figure out the starting address of the character to be output in the large font
;Then i took the value in that address and looked at the most significant bit to determine whether or not to print the ascii character attached to 1 or the character attached to 0 and printed the correct one 
;I then left-shifted the value from the address by multiplying it by 2 and looked at the msb again and printed it. I repeated this process a total of 8 times.
;After all 8 characters from the line are printed into the console, I incremented the address value by 1 and printed out the next 8 characters. I did this a total of 16 times for an overall grid of
;8 columns and 16 rows that forms the larger character. 
;R0 This register will be used to hold the ascii character hex values to be printed to the console
;R1 This register will be used to hold the current address of the line fron the font table the code is using (address pointer)
;R2 This register will be used to count the total number of lines of the character we have printed so far
;R3 This register will be used to count the number of sub-characters in each line we have printed so far
;R4 This register will be used in the math function to count number of additions that have been performed (emulating multiplication)
;R5 This register will be used for miscellanous functions like holding R1's value to be used in multiplication in the math step
;R6 This register will be used to hold the contents of the address pointed to be R1 and will be left shifted after each character in a line is printed to check whethere to print a 0 or a 1


		;INIT
		.ORIG x3000
		AND  R0,R0,0	;CHARACTERS
		AND  R1,R1,0	;FONT LINE ADDRESS
		AND  R2,R2,0	;MAINLOOP COUNTER
		AND  R3,R3,0	;SUBLOOP COUNTER
		AND  R4,R4,0	;MATHLOOP COUNTER
		AND  R5,R5,0	;MISC FUNCTIONS
		AND  R6,R6,0	;BIT SHIFTER FOR PRINTLOOPS
		
		
		LDI  R1,CHAR	;Loads the hex value from the char to be printed into R1
		ADD  R2,R2,#8	;This is the counter to be incremented after every line (MAINLOOP)
		ADD  R2,R2,#8	;Have to add 8 twice cuz 16 is out of the range of imm5
		ADD  R3,R3,#8	;This is the counter to be incremented after every character printed to the console (SUBLOOP)
		ADD  R4,R4,#8	;This is the counter for the math needed to find the starting location in FONT_DATA for the character. 
		ADD  R4,R4,#7	;Have to add 8 then 7 twice b/c you only need to add R1 to itself 15 times to emulate multiplying by 16
		ADD  R5,R5,R1   ;Loads R1 into R5 to make it easier to do the math step with emulating multiplication

MATHLOOP	ADD  R1,R1,R5	;Adds the starting R1 value to the running total
		ADD  R4,R4,#-1	;Decrements the counter
		BRnp MATHLOOP	;Continues looping until counter is 0
		LEA  R5,FONT_DATA ;Loads the address of the first bit of font data into R5
		ADD  R1,R1,R5	;R1 Should now have the address of the starting line in the font data in it. 
		BR   SUBLOOP1   ;Goes to the first line of printing

MAINLOOP	ADD  R1,R1,#1	;R1 Should now have the next address of the font data in it. 
		ADD  R2,R2,#-1  ;Decrement R2, this will happen a total of 16 times
		AND  R3,R3,0    ;Reset R3
		ADD  R3,R3,#8   ;This will be used again to count 8 characters per line
		ADD  R2,R2,#0	;Sets the cc with the R2 counter again
		BRz  STOP	;Once all 16 lines are done R2=0 and program halts
		BRnp SUBLOOP1   ;Goes to the next line of printing
		

SUBLOOP1	LDR  R6,R1,#0	;Loads R6 with the contents of the font address line specified by R1
		BRn  NEGLOOP	;If R6 is negative (MSB = 1) it goes to print the 1 character
		BRzp POSLOOP	;If R6 is positive (MSB = 0) it goes to print the 0 character
 
SUBLOOP2	ADD  R6,R6,0	;Sets cc with R6
		BRn  NEGLOOP	;If R6 is negative (MSB = 1) it goes to print the 1 character
		BRzp POSLOOP	;If R6 is positive (MSB = 0) it goes to print the 0 character

SUBSUBLOOP	ADD  R6,R6,R6	;Multiplies R6 (the value of the current line) by 2, effectively left-shifting it	
		ADD  R3,R3,#-1	;Decrements the counter for number of characters printed
		BRnp SUBLOOP2	;If the counter isnt 0 it moves back to the loop to determine and print the next character in the line
		LD   R0,NEWLINE	;If counter = 0, This step runs which loads R0 with the new line character
		OUT		;Prints the new line character to the console
		BR  MAINLOOP	;Heads back to the main loop to increment the address pointer and decrement the line # counter. 

POSLOOP		LDI  R0,ZERO	;Loads R0 with the hex value for the ascii character defined in the case file
		OUT 		;Prints the ascii char in R0 to the console
		BR   SUBSUBLOOP	;Heads to the step to increment (left shift) the font line's value

NEGLOOP		LDI  R0,ONE	;Loads R0 with the hex value for the ascii character defined in the case file
		OUT		;Prints the ascii char in R0 to the console
		BR   SUBSUBLOOP	;Heads to the step to increment (left shift) the font line's value

STOP		HALT
ZERO		.FILL x5000	;The ascii character for a 0 in the font data code
ONE		.FILL x5001	;The ascii character for a 1 in the font data code
CHAR		.FILL x5002	;The overall large ascii character to be printed
NEWLINE 	.FILL x000A
